function openModal1() 
{
  document.getElementById('animagifc').style.display = 'inline-block';
} 
function closeModal1() 
{
  document.getElementById('animagifc').style.display = 'none';
}
$("#animagif").hover(
  function (animagif) {
    openModal1();
  }, 
  function (animagif) {    
    closeModal1();
  }
);
